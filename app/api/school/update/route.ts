import { NextResponse } from "next/server";
import { getServerSession } from "next-auth";
import { authOptions } from "@/lib/authOptions";
import prisma from "@/lib/db";
import { nameToSubdomain } from "@/lib/subdomain";

export async function PUT(req: Request) {
  try {
    const session = await getServerSession(authOptions);
    const body = await req.json();
    const { name, address, location, subdomain } = body;

    if (!session)
      return NextResponse.json({ message: "Unauthorized" }, { status: 401 });


    // Read user's schoolId from primary (optional: read from replica if acceptable)
    const user = await prisma.user.findUnique({
      where: { id: session.user.id },
    });

    if (!user?.schoolId) {
      return NextResponse.json(
        { message: "You have not created a school yet" },
        { status: 400 }
      );
    }

    const data: { name?: string; address?: string; location?: string; subdomain?: string | null } = {};
    if (name !== undefined) data.name = name;
    if (address !== undefined) data.address = address;
    if (location !== undefined) data.location = location;
    if (subdomain !== undefined) {
      data.subdomain = typeof subdomain === "string" && subdomain.trim()
        ? nameToSubdomain(subdomain.trim())
        : null;
    }

    // âœ… UPDATE school on primary
    const updated = await prisma.school.update({
      where: { id: user.schoolId },
      data,
    });

    return NextResponse.json(
      { message: "School updated", updated },
      { status: 200 }
    );
  } catch (error) {
    console.error("School update error:", error);
    return NextResponse.json(
      { message: "Error updating school" },
      { status: 500 }
    );
  }
}
